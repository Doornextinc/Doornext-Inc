-- 054: DoorDash-style order stacking / multi-stop routing
--
-- New additions:
--   driver_route_plans  — ordered stop list for a multi-stop run
--   order_stack_offers  — pending stack offers pushed to active drivers
--   settings rows       — stack_bonus_amount, stack_limit
