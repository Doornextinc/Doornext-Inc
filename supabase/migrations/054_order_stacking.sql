  ('stack_limit',               '2'),      -- max orders a driver can carry at once
  ('stack_bonus_amount',        '1.50'),   -- $ added to driver_payout for each stacked order
  ('stack_pickup_radius_km',    '3.0'),    -- max km between two pickup locations
  ('stack_dropoff_radius_km',   '4.0'),    -- max km between two dropoff locations
  ('stack_detour_pct',          '40'),     -- max % route-length increase allowed
  ('stack_offer_timeout_secs',  '45')      -- seconds driver has to accept a stack offer
ON CONFLICT (key) DO NOTHING;

-- ── 4. Fast compound index for stack-candidate queries ────────────────────────
-- Finds unassigned preparing/ready orders quickly when checking for stacking.
CREATE INDEX IF NOT EXISTS idx_orders_stack_candidates
  ON public.orders (status, created_at)
  WHERE nexter_id IS NULL AND status IN ('preparing', 'ready');

-- ── 5. RPC: accept_order_atomic ───────────────────────────────────────────────
-- Atomically accepts a single order OR a stacked pair under one transaction.
-- Called by the Next.js accept-order API route (service role).
--
-- Parameters:
--   p_driver_id      — driver's auth.users.id
--   p_order_id       — the order being accepted now
--   p_pickup_pin     — 4-char PIN generated by API
--   p_group_id       — UUID to use for order_group_id (NULL → single order)
--   p_existing_ids   — existing order IDs in the group (NULL or empty → single order)
--   p_bonus_amount   — stack bonus to add to driver_payout (0 if single)
--
-- Returns:
--   'ok'             — accepted successfully
--   'taken'          — another driver beat us to it (race)
--   'bad_state'      — order not in accepting state

CREATE OR REPLACE FUNCTION public.accept_order_atomic(
  p_driver_id    uuid,
  p_order_id     uuid,
  p_pickup_pin   text,
  p_group_id     uuid    DEFAULT NULL,
  p_existing_ids uuid[]  DEFAULT NULL,
  p_bonus_amount numeric DEFAULT 0
)
RETURNS text
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_count  int;
BEGIN
  -- Accept the new order atomically
  UPDATE orders
  SET
    nexter_id        = p_driver_id,
    status           = 'driver_assigned',
    pickup_pin       = p_pickup_pin,
    pin_attempts     = 0,
    order_group_id   = COALESCE(p_group_id, NULL),
    driver_payout    = driver_payout + p_bonus_amount,
    updated_at       = now()
  WHERE
    id           = p_order_id
    AND status   IN ('preparing', 'ready')
    AND nexter_id IS NULL;

  GET DIAGNOSTICS v_count = ROW_COUNT;
  IF v_count = 0 THEN
    RETURN 'taken';
  END IF;

  -- If stacking, stamp the group ID onto existing orders too
  IF p_group_id IS NOT NULL AND p_existing_ids IS NOT NULL AND array_length(p_existing_ids, 1) > 0 THEN
    UPDATE orders
    SET order_group_id = p_group_id,
        updated_at     = now()
    WHERE id = ANY(p_existing_ids)
      AND nexter_id = p_driver_id;
  END IF;

  RETURN 'ok';
END;
$$;

REVOKE EXECUTE ON FUNCTION public.accept_order_atomic(uuid, uuid, text, uuid, uuid[], numeric) FROM PUBLIC;
REVOKE EXECUTE ON FUNCTION public.accept_order_atomic(uuid, uuid, text, uuid, uuid[], numeric) FROM authenticated;
