-- ── 1. driver_route_plans ─────────────────────────────────────────────────────
-- Stores the optimised stop sequence for a multi-stop run.
-- stops is an ordered JSONB array of:
--   {
--     "seq":      number,          -- 1-based position in route
--     "type":     "pickup"|"dropoff",
--     "order_id": "uuid",
--     "lat":      number,
--     "lng":      number,
--     "label":    "string",        -- maker name (pickup) or street (dropoff)
--     "done":     boolean
--   }

CREATE TABLE IF NOT EXISTS public.driver_route_plans (
  id              uuid        PRIMARY KEY DEFAULT gen_random_uuid(),
  driver_id       uuid        NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
  order_group_id  uuid        NOT NULL,
  stops           jsonb       NOT NULL DEFAULT '[]',
  total_distance_km numeric(8,3),         -- sum of all legs
  created_at      timestamptz NOT NULL DEFAULT now(),
  updated_at      timestamptz NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS idx_driver_route_plans_driver
  ON public.driver_route_plans (driver_id)
  WHERE stops != '[]'::jsonb;

CREATE INDEX IF NOT EXISTS idx_driver_route_plans_group
  ON public.driver_route_plans (order_group_id);

ALTER TABLE public.driver_route_plans ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Drivers manage own route plans"
  ON public.driver_route_plans
  USING (auth.uid() = driver_id);

-- ── 2. order_stack_offers ─────────────────────────────────────────────────────
-- When a driver is already mid-delivery, the system can push a compatible
-- nearby order as a "stack offer".  Driver has stack_offer_timeout_secs to
-- accept or decline before it expires.

CREATE TABLE IF NOT EXISTS public.order_stack_offers (
  id          uuid        PRIMARY KEY DEFAULT gen_random_uuid(),
  driver_id   uuid        NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
  order_id    uuid        NOT NULL REFERENCES public.orders(id) ON DELETE CASCADE,
  score       numeric(6,3),               -- compatibility score (higher = better fit)
  status      text        NOT NULL DEFAULT 'pending'
                CHECK (status IN ('pending','accepted','rejected','expired')),
  expires_at  timestamptz NOT NULL,
  created_at  timestamptz NOT NULL DEFAULT now(),
  UNIQUE (driver_id, order_id)
);

CREATE INDEX IF NOT EXISTS idx_stack_offers_driver_pending
  ON public.order_stack_offers (driver_id, status, expires_at)
  WHERE status = 'pending';

ALTER TABLE public.order_stack_offers ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Drivers read own stack offers"
  ON public.order_stack_offers FOR SELECT
  USING (auth.uid() = driver_id);

