-- ── 6. RPC: mark_route_stop_done ──────────────────────────────────────────────
-- Updates a single stop's "done" flag in the driver_route_plans.stops JSONB array.
-- Returns the updated stops array.

CREATE OR REPLACE FUNCTION public.mark_route_stop_done(
  p_driver_id    uuid,
  p_order_id     uuid,
  p_stop_type    text   -- 'pickup' or 'dropoff'
)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_stops    jsonb;
  v_new_stops jsonb;
  v_stop     jsonb;
  v_idx      int;
BEGIN
  SELECT stops INTO v_stops
  FROM driver_route_plans
  WHERE driver_id = p_driver_id
  ORDER BY created_at DESC
  LIMIT 1;

  IF v_stops IS NULL THEN
    RETURN '[]'::jsonb;
  END IF;

  -- Rebuild array with the matched stop marked done
  v_new_stops := '[]'::jsonb;
  FOR v_idx IN 0 .. jsonb_array_length(v_stops) - 1 LOOP
    v_stop := v_stops -> v_idx;
    IF (v_stop->>'order_id') = p_order_id::text
       AND (v_stop->>'type') = p_stop_type
    THEN
      v_stop := v_stop || '{"done":true}'::jsonb;
    END IF;
    v_new_stops := v_new_stops || jsonb_build_array(v_stop);
  END LOOP;

  UPDATE driver_route_plans
  SET stops = v_new_stops, updated_at = now()
  WHERE driver_id = p_driver_id
    AND stops @> jsonb_build_array(jsonb_build_object('order_id', p_order_id, 'type', p_stop_type));

  RETURN v_new_stops;
END;
$$;

REVOKE EXECUTE ON FUNCTION public.mark_route_stop_done(uuid, uuid, text) FROM PUBLIC;
